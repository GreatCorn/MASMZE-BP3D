Vector32DLerp PROC EXPORT A:BPPtr, B:BPPtr, T:REAL4
	mov pax, A
	mov pcx, B
	
	fld REAL4 PTR [pcx]
	fsub REAL4 PTR [pax]
	fmul T
	fadd REAL4 PTR [pax]
	fstp REAL4 PTR [pax]
	
	fld REAL4 PTR [pcx+8]
	fsub REAL4 PTR [pax+8]
	fmul T
	fadd REAL4 PTR [pax+8]
	fstp REAL4 PTR [pax+8]
	ret
Vector32DLerp ENDP